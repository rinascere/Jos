// Return 4 continuous pages to chunck list. Do the following things:
//	1. Check whether the four pages int the list are continue, Return -1 on Error
//	2. Add the pages to the chunck list.
//	
//	Return 0 if everything ok
int
page_free_4pages(struct Page *pp)
{
	if(check_continuous(pp) == 0){
		return -1;
	}
	struct Page* fls = pp;
	chunck_list.pp_link = fls;
	while(chunck_list.pp_link != NULL)
	{
		fls = chunck_list.pp_link;
		chunck_list.pp_link = chunck_list.pp_link->pp_link;
	}
	struct Page* tmp;
	tmp = pp->pp_link->pp_link->pp_link;
	pp->pp_link->pp_link = pp;
	tmp->pp_link = pp->pp_link->pp_link;
	tmp->pp_link->pp_link = pp->pp_link;
	pp->pp_link = page_free_list;
	page_free_list = tmp;
	return 0;
}

//
// Return a page to the free list.
// (This function should only be called when pp->pp_ref reaches 0.)
//
void
page_free(struct Page *pp)
{
	// Fill this function in
	//pp->pp_ref--;
	if(pp->pp_ref == 0){
		pp->pp_link = page_free_list;
		page_free_list = pp;
	}
}

//
// Decrement the reference count on a page,
// freeing it if there are no more refs.
//
void
page_decref(struct Page* pp)
{
	if (--pp->pp_ref == 0)
		page_free(pp);
}

// Given 'pgdir', a pointer to a page directory, pgdir_walk returns
// a pointer to the page table entry (PTE) for linear address 'va'.
// This requires walking the two-level page table structure.
//
// The relevant page table page might not exist yet.
// If this is true, and create == false, then pgdir_walk returns NULL.
// Otherwise, pgdir_walk allocates a new page table page with page_alloc.
//    - If the allocation fails, pgdir_walk returns NULL.
//    - Otherwise, the new page's reference count is incremented,
//	the page is cleared,
//	and pgdir_walk returns a pointer into the new page table page.
//
// Hint 1: you can turn a Page * into the physical address of the
// page it refers to with page2pa() from kern/pmap.h.
//
// Hint 2: the x86 MMU checks permission bits in both the page directory
// and the page table, so it's safe to leave permissions in the page
// more permissive than strictly necessary.
//
// Hint 3: look at inc/mmu.h for useful macros that mainipulate page
// table and page directory entries.
//
