				//return (pte_t *)(pte[PTX(va)]);
				pgdir[PDX(va)] = PADDR(page2kva(new_page))|PTE_U|PTE_W|PTE_P;
               			pte = (void*)KADDR(PTE_ADDR(pgdir[PDX(va)]));
